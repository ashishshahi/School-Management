    private void add_fee_occuranceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_fee_occuranceActionPerformed
        try {
            Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
            if(con != null){
                System.out.println("Connection Sucessfully");
            }
            String sql = "Insert Into FeeOccurance (fee_occurance, fee_cateogry_name) values (?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,fee_occurance.getText().trim());
            pst.setString(2,fee_cateogry_name.getText().trim());
            int rowsInserted = pst.executeUpdate();
            if(rowsInserted>0){
                JOptionPane.showMessageDialog(null, "Sucessfully Added Fee Occurance Type.");
                list_fee_occurance.removeAllItems();
                SelectFeeOccuranceType();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_add_fee_occuranceActionPerformed

