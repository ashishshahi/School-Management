    public void SelectFeeOccuranceType(){
        try {
             Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
           Statement statement = con.createStatement();
           String selectedquery = "select * from FeeOccurance";
             ResultSet rs = statement.executeQuery(selectedquery);
             while(rs.next()){
                 list_fee_occurance.addItem(rs.getString("fee_occurance"));
                 fee_occurence.addItem(rs.getString("fee_occurance"));
                 list_fee_catogry.addItem(rs.getString("fee_cateogry_name"));
                 fee_cateogry.addItem(rs.getString("fee_cateogry_name"));
             }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
