    private void add_expenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_expenseActionPerformed
        try {
            Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
            if(con != null){
                System.out.println("Connection Sucessfully");
            }
            String sql = "Insert Into ExpenseList (expense_type, expense_date, expense_amount, note) values (?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,expense_type_list.getSelectedItem().toString());
            pst.setDate(2, new java.sql.Date(expense_date.getDate().getTime()));
            pst.setString(3, expense_amount.getText().trim());
            pst.setString(4, expense_note.getText().trim());
            int rowsInserted = pst.executeUpdate();
            if(rowsInserted>0){
                JOptionPane.showMessageDialog(null, "Sucessfully Added Expenses Record.");
                expense_type_list.removeAllItems();
                SelectExpenseType();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_add_expenseActionPerformed

