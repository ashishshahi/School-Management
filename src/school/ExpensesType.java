    public void SelectExpenseType(){
        try {
             Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
           Statement statement = con.createStatement();
           String selectedquery = "select expense_type from expensetype";
             ResultSet rs = statement.executeQuery(selectedquery);
             while(rs.next()){
                 expense_type_list.addItem(rs.getString("expense_type"));
                 expense_type_its_payment.addItem(rs.getString("expense_type"));
             }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
