    private void add_expense_typeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_expense_typeActionPerformed
        try {
            Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
            if(con != null){
                System.out.println("Connection Sucessfully");
            }
            String sql = "Insert Into expensetype (expense_type, description) values (?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,expense_type.getText().trim());
            pst.setString(2,description.getText().trim());
            int rowsInserted = pst.executeUpdate();
            if(rowsInserted>0){
                JOptionPane.showMessageDialog(null, "Sucessfully Added Expense Type.");
                expense_type_list.removeAllItems();
                SelectExpenseType();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_add_expense_typeActionPerformed

