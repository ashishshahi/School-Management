    public void enroll_no(){
        try {
            Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
            Statement statement = con.createStatement();
            String selectedquery = "select enrollment_no from students order by id desc limit 1";
            ResultSet rs = statement.executeQuery(selectedquery);
            while(rs.next()){
                String roll = rs.getString("enrollment_no");
                int enroll = 1 + Integer.parseInt(roll);
                enroll_no.setText(Integer.toString(enroll));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
