    private void save_employeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_employeeActionPerformed
        try {
            Connection con = (Connection) DriverManager.getConnection(Config.DbUrl,Config.DbUser,Config.DbPass);
            String sql = "INSERT INTO school.employees (id, first_name, last_name, gender, dob, emp_photo, mobile, landline, address, email, h_qualification, p_marks, university, experince, prev_orgnisation) values (NULL,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,fname.getText().toString());
            pst.setString(2, l_name.getText().trim());
            pst.setString(3, emp_gender.getSelectedItem().toString());
            pst.setDate(4, new java.sql.Date(emp_dob.getDate().getTime()));
            pst.setString(5, path_of_emp_photos.getText().trim());
            pst.setString(6, emp_mob.getText().trim());
            pst.setString(7, emp_land_line.getText().trim());
            pst.setString(8, emp_address.getText().trim());
            pst.setString(9, emp_mail_id.getText().trim());
            pst.setString(10, high_qualification.getText().trim());
            pst.setString(11, percentage_marks.getText().trim());
            pst.setString(12, university.getText().trim());
            pst.setString(13, experince.getText().trim());
            pst.setString(14, prev_organisation.getText().trim());
            int rowsInserted = pst.executeUpdate();
            if(rowsInserted>0){
                JOptionPane.showMessageDialog(null, "Employee Record Added Sucessfully .");
                ClearEmployee();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_save_employeeActionPerformed

